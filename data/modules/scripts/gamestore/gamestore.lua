  if category.name == "Outfits" and category.offers then
    for m, offer in ipairs(category.offers) do
      offer.id = runningId
      runningId = runningId + 1

      if not offer.type then
        offer.type = GameStore.OfferTypes.OFFER_TYPE_NONE
      end
    end
  end
end
